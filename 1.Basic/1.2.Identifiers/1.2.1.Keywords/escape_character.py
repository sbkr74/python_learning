# \n -> new line
# \t -> horizontal tab
# \r -> carriage return
# \b -> backspace
# \f -> form Feed
# \v -> vertical tab
# to use special character use \ and special character
# \' -> single quote
# \" -> Double quote
# \\ -> backslash symbol

print("Python program \nBasic to advance!")
print("Python program from \f\fBasic to advance Tutorial.")
print("Python program \v\vBasic to advance!")
print("Python program \t\tBasic to advance!")
print("Python program \r     Basic to advance!")
print("Python program \n\t    \rBasic to advance!")
