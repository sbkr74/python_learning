# We can represent int values in 4 ways
# 1. Decimal form
# 2. Binary form
# 3. Octal form
# 4. Hexa decimal form
# ======================================
# 1. Decimal form
a = 10 
print(a)
print(type(a))

# 2. Binary form
# val = 0B123
b = 5
val = bin(b)
print(val)
print(type(val))

# 3. Octal form
# val = oct(0B1111)
val = oct(0b111)
print(val)
print(type(val))

# 4. Hexa decimal form
val = hex(0B1111)
print(val)
print(type(val))