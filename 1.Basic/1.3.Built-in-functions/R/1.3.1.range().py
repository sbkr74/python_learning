# range(): It is used to generate immutable sequence of numbers.
# range(stop)
seq = list(range(10))
print(seq)

# range(start,stop)
seq = list(range(1,11))
print(seq)

# range(start,stop,step)
seq = list(range(1,11,2))
print(seq)

seq = list(range(0,10,2))
print(seq)

seq = list(range(10,0,-1))
print(seq)