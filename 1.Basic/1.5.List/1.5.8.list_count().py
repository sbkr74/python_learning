# count(): It is used to count the occurence of an item in list.
items = ['Python','Objective C', 'C++','Bash Script','Java Core','Ruby','Scala','JavaScript']
print(items.count('Scala'))     # 1 
print(items.count('C'))         # 0 (as it is not present in list)
ages = [22, 19, 24, 25, 26, 24, 25, 24]
print(ages.count(24))           # 3
