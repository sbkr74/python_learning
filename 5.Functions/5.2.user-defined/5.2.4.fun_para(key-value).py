def print_fullname(firstname, lastname):
    space = ' '
    full_name = firstname  + space + lastname
    print(full_name)
print_fullname(firstname = 'Shubham', lastname = 'Biruly')

def add_two_numbers (num1, num2):
    total = num1 + num2
    print(total)
add_two_numbers(num2 = 3, num1 = 2) # Order does not matter